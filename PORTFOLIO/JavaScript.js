// Typing animation for the subtitle
function startSubtitleAnimation() {
    const phrases = ["I AM A WEB DEVELOPER", "I AM A PROGRAMMER"];
    const element = document.getElementById('developer-title');
    let currentPhrase = 0;
    let currentLetter = 0;
    let isDeleting = false;
    let typingSpeed = 100;
    let pauseTime = 1500;

    function type() {
        const currentText = phrases[currentPhrase];

        if (isDeleting) {
            // Delete text
            element.textContent = currentText.substring(0, currentLetter - 1);
            currentLetter--;
            typingSpeed = 50; // Faster when deleting
        } else {
            // Type text
            element.textContent = currentText.substring(0, currentLetter + 1);
            currentLetter++;
            typingSpeed = 100; // Normal typing speed
        }

        // Check if current phrase is complete
        if (!isDeleting && currentLetter === currentText.length) {
            typingSpeed = pauseTime; // Pause at the end
            isDeleting = true;
        } else if (isDeleting && currentLetter === 0) {
            isDeleting = false;
            currentPhrase = (currentPhrase + 1) % phrases.length; // Move to next phrase
            typingSpeed = 500; // Pause before starting next phrase
        }

        setTimeout(type, typingSpeed);
    }

    // Start the typing animation
    type();
}

// Show skill detail in modal
function showSkillDetail(skillName) {
    // Find the skill in our data
    let skillDetail = null;
    let skillTopics = null;
    let skillCategory = null;

    for (const category of portfolioData.skills.categories) {
        for (const skill of category.skills) {
            if (skill.name === skillName) {
                skillDetail = skill.detail;
                skillTopics = skill.topics;
                skillCategory = category.name;
                break;
            }
        }
        if (skillDetail) break;
    }

    if (skillDetail) {
        document.getElementById('modal-skill-name').textContent = skillName;
        document.getElementById('modal-skill-description').textContent = skillDetail;

        // Add topics for this skill
        const topicsContainer = document.getElementById('modal-skill-topics');
        topicsContainer.innerHTML = '';

        if (skillTopics && skillTopics.length > 0) {
            skillTopics.forEach(topic => {
                const topicEl = document.createElement('div');
                topicEl.className = 'topic-item';
                topicEl.innerHTML = `<h5>${topic}</h5>`;
                topicsContainer.appendChild(topicEl);
            });
        } else {
            topicsContainer.innerHTML = '<p>No specific topics listed for this skill.</p>';
        }

        // Find projects that use this skill
        const projectsContainer = document.getElementById('modal-skill-projects');
        projectsContainer.innerHTML = '';

        portfolioData.projects.items.forEach(project => {
            if (project.tags.includes(skillName)) {
                const projectEl = document.createElement('div');
                projectEl.className = 'project-item';
                projectEl.innerHTML = `
                            <h5>${project.title}</h5>
                            <p>${project.description}</p>
                            <a href="${project.githubUrl}" class="project-link" target="_blank">View Project</a>
                        `;
                projectsContainer.appendChild(projectEl);
            }
        });

        // If no projects found, display a message
        if (projectsContainer.children.length === 0) {
            projectsContainer.innerHTML = '<p>No specific projects listed for this skill.</p>';
        }

        // Show the modal
        document.getElementById('skill-modal').style.display = 'flex';
    }
}

